print("enter two numbers")

a=int(input())      
b=int(input())
print("------RESULT------")
print(a , "  +   " , b , "="  ,  a+b)  #Addition
print(a , "  -   " , b , "="  ,  a-b)  #Substraction
print(a , "  *   " , b , "="  ,  a*b)  #Multiplication
print(a , "  /   " , b , "="  ,  a/b)  #Division
print(a , "  //  " , b , "="  ,  a//b) #Integer Division
print(a , "  %   " , b , "="  ,  a%b)  #Modules
print(a , "  ^   " , b , "="  ,  a**b) #Exponent

# 1. input() allows read value as string from console
# 2. use int() to convert string to integer
# 3. in print() function objects needs to be separated using comma ( , ).
# 4. // operator allows to perfrom integer division (with out fraction)
# 5. Exponent operation can be performed using ** operator 
